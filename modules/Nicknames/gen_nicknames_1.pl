my %rootnames=();
my %nicknames=();

# generate data to be used in Nicknames.pm

foreach my $nickname ( keys %names ) {
	#print $nickname, "\n";
	# does it end with E?
	if ( $nickname =~ /^\w+E$/ ) {
		#print "$nickname\n";
		my $s = $nickname;
		# remove E at end
		$s =~ s/(^\w+)E$/$1/;
		#print "$s\n";
		foreach my $suffix ( qw{y ie ey}){
			push @{$rootnames{$names{$nickname}}}, $s . $suffix;
			#push@{$r        {a          }}, 4;
			#print "$s$suffix - $names{$nickname}\n";
			$nicknames{$s . $suffix} = $names{$nickname};
		}
	} else {
		#print "$nickname - $names{$nickname}\n";
		push @{$rootnames{$names{$nickname}}}, $nickname;
		$nicknames{$nickname} = $names{$nickname};
	}
}

my $delimiter=':';

foreach my $rootname ( keys %rootnames ) {
	# nickname:rootname output
	$rootname = ucfirst(lc($rootname));
	foreach my $nickname ( @{$rootnames{$rootname}}) {
		$nickname = ucfirst(lc($nickname));
		print "${nickname}${delimiter}${rootname}\n";
	}
}

